
public class programs {
	
	public static void main(String[] args) {
		System.out.println("===for loop===");
		int p=1;
		for(p=1;p<=19;p++) {
			System.out.println(p+"*"+19+"="+p*19);
	}
		System.out.println("===while===");
		int i=1;
	while(i<=20) {
		System.out.println(i+"*"+19+"="+19*i);
		i++;
	}
	System.out.println("====do-while===");
	int j=1;
	do {
		System.out.println(j+"*"+19+"="+19*j);
		j++;
	}while(j<=20);

	}		
}
